function trafficLight(light){
     if(light == "red" || light == "Red"){
        console.log("Red: Stop!");
    }

    else if(light == "yellow" || light == "Yellow"){
        console.log("Yellow: Be Carefull!");
    }

    else if(light == "green" || light == "Green"){
        console.log("Green: Go!");
    }
}

trafficLight("Red");